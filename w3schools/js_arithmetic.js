/*
JavaScript Arithmetic Operators
Arithmetic operators perform arithmetic on numbers (literals or variables).

Operator	Description
+	        Addition
-	        Subtraction
*	        Multiplication
**	        Exponentiation (ES2016)
/	        Division
%	        Modulus (Remainder)
++	        Increment
--	        Decrement
*/

// Addition
let h = 5;
let i = 2;
let j = h + i;
console.log(j) // 7

// Subtraction
let k = 5;
let l = 2;
let m = k - l;
console.log(m) // 3

// Multiplication
let n = 5;
let o = 2;
let p = n * o;
console.log(p) // 10

// Exponentiation
let a = 5;
let b = 2;
let c = a ** b;
console.log(c) // 25

// Division
let q = 5;
let r = 2;
let s = q / r;
console.log(s) // 2.5

// Modulus (Remainder)
let t = 5;
let u = 2;
let v = t % u;
console.log(v) // 1

// Increment
let w = 5;
w++;
console.log(w) // 6

// Decrement
let x = 5;
x--;
console.log(x) // 4